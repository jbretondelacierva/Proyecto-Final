package com.grupopat.practicafinal.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.relational.core.mapping.Column;
import org.springframework.data.relational.core.mapping.Table;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.util.Objects;

@Data
@Builder
@AllArgsConstructor
@NoArgsConstructor
@Table("CONTACTOS")
public class ContactosModel {
    
    private @Column("ID_CONTACTO") @Id Integer id_contacto;
    private @Column("USERNAME") String username;
    private @Column("NOMBRE") String nombre;
    private @Column("APELLIDO") String apellido;
    private @Column("MAIL") String mail; 
    private @Column("DIRECCION") String direccion; 



    public Integer getId_contacto() {
        return id_contacto;
    }

    public void setId_contacto(Integer id_contacto) {
        this.id_contacto = id_contacto;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        ContactosModel that = (ContactosModel) o;
        return Objects.equals(id_contacto, that.id_contacto);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id_contacto);
    }
}
